package com.mem.xls;


public class RowReaderException extends RuntimeException {

	private static final long serialVersionUID = 1138134673328992808L;

	public RowReaderException() {
		super();
	}

    public RowReaderException(String message, Throwable cause) {
		super(message, cause);
	}

    public RowReaderException(String message) {
		super(message);
	}

    public RowReaderException(Throwable cause) {
		super(cause);
	}

}
